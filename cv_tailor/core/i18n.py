# ── Font overrides per locale (for CJK scripts) ───────────────────────────────
LOCALE_FONTS = {
    "en": None,   # Use default Segoe UI
    "fr": None,
    "de": None,
    "es": None,
    "ja": ("Yu Gothic UI", "Hiragino Sans", "MS Gothic", "Segoe UI"),
    "zh": ("PingFang SC", "Microsoft YaHei", "SimHei", "Segoe UI"),
}

def get_ui_font(locale: str, base_font: tuple) -> tuple:
    """Return locale-appropriate font, keeping size/weight from base_font."""
    overrides = LOCALE_FONTS.get(locale)
    if not overrides:
        return base_font
    size   = base_font[1] if len(base_font) > 1 else 10
    weight = base_font[2] if len(base_font) > 2 else ""
    return (overrides[0], size, weight) if weight else (overrides[0], size)


