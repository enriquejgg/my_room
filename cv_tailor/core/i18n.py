def get(key: str, locale: str = "en", **kwargs) -> str:
    """
    Return translated string for key in locale.
    Falls back to English if key or locale not found.
    Supports .format() kwargs (e.g. get("mode.status.done", n=3, s="s"))
    """
    lang_dict = _T.get(locale, _T["en"])
    text = lang_dict.get(key) or _T["en"].get(key, f"[{key}]")
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text


def get_steps(locale: str = "en") -> list:
    """Return list of (number_str, label) tuples for the stepper."""
    return [
        ("1", get("step.1", locale)),
        ("2", get("step.2", locale)),
        ("3", get("step.3", locale)),
        ("4", get("step.4", locale)),
    ]


